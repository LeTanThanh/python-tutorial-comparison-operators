if __name__ == "__main__":
  # Less than operator (<)

  """
  left_value < right_value
  """

  print(10 < 20)
  print(30 < 20)

  print("apple" < "banana")
  print("banana" < "apple")

  """
  The expression "apple" < "banana" returns True because the letter "a" in "apple" is before the letter "o" in "orange".
  Similarly, the "banana" < "apple" returns False because the letter "b" in "banana" is after the letter "a"
  """

  x = 10
  y = 20
  print(x < y)
  print(y < x)

  # Less than or equal to operator (<=)

  """
  left_value <= right_value
  """

  print(20 <= 20)
  print(10 <= 20)
  print(30 <= 30)

  x = 10
  y = 20
  print(x <= y)
  print(y <= x)

  # Greater than operator (>)

  """
  left_value > right_value
  """

  print(20 > 10)
  print(20 > 20)
  print(10 > 20)

  print("apple" > "orange")
  print("orange" > "apple")
